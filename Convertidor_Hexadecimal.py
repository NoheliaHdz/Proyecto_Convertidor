#usuarion ingresa el numero que desee pasar a hexadecimal
num_decimal = int(input("Ingrese el numero decimal: "))  

# funcion utilizada para pasa un decimal a hexadecimal
num_hexadecimal = hex(num_decimal)

# Impresion del resultados
print(f"La representacion hexadecimal de {num_decimal} es: {num_hexadecimal}")
